    private void concluidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_concluidoActionPerformed
        
        try {
            java.sql.PreparedStatement pst = conecta.conn.prepareStatement("update Estados set nome_estado=?,sigla_estado=? where id_estado =?");
            pst.setString(1, textonome.getText());
            pst.setString(2, textosigla.getText());
            pst.setInt(3, Integer.parseInt(textocodigo.getText()));
            pst.execute();
            JOptionPane.showMessageDialog(null, "Dados alterados com sucesso!");
        
        } 
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro com o Banco de Dados! \n Erro: "+ex.getMessage());
        }
        
        btvoltartela.setVisible(true);
        btadd.setVisible(true);
        btview.setVisible(true);
        txcodigo.setVisible(true);
        btclear.setVisible(true);
        PainelBt.setVisible(true);
        try {
            conecta.executaSQLQuery("select * from Estados");
            conecta.rs.first();
            textocodigo.setText(String.valueOf(conecta.rs.getInt("id_estado")));
            textonome.setText(conecta.rs.getString("nome_estado"));
            textosigla.setText(conecta.rs.getString("sigla_estado"));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Falha na leitura do Banco de Dados");
        }
        textonome.setEnabled(false);
        textosigla.setEnabled(false);
        btmodify.setVisible(true);
        concluido.setVisible(false);
        btcancelar.setVisible(false);
    }//GEN-LAST:event_concluidoActionPerformed

