    private void btsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveActionPerformed

        try {
            java.sql.PreparedStatement pst = conecta.conn.prepareStatement("insert into Estados (nome_estado,sigla_estado)values(?,?)");
            if (textonome.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Não é permitido campo vazio. Digite o nome do Estado para salvar!");
            } else {
                pst.setString(1, textonome.getText());
            }
            if (textosigla.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Não é permitido campo vazio. Digite a sigla do estado para salvar!");
            } else {
                pst.setString(2, textosigla.getText());
            }
            pst.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Gravado com sucesso!");
            textonome.setText("");
            textosigla.setText("");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Nenhum dado foi gravado. Insira os valores corretos e tente novamente!");
            textonome.setText("");
            textosigla.setText("");
        }
        textonome.setEnabled(false);
        textosigla.setEnabled(false);
        btsave.setVisible(false);
        btadd.setVisible(true);
        btview.setVisible(true);
        txcodigo.setVisible(true);
        textocodigo.setVisible(true);
        btclear.setVisible(false);
        PainelBt.setVisible(false);
        btmodify.setVisible(false);

    }//GEN-LAST:event_btsaveActionPerformed

